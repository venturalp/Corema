                    <h2>1. Porque devo registrar minha marca?</h2>
                    <p>R: Porque SOMENTE o registro concedido através do Órgão Federal INPI – Instituto Nacional da Propriedade Industrial tem poderes legais para garantir a exclusividade da marca ao seu proprietário.</p>
                    <h2>2. Já faz anos que utilizo minha marca, então já sou o dono certo?</h2>
                    <p>R: ERRADO! De acordo com a Lei da Propriedade Industrial é dono da marca apenas a pessoa que a registrou, independentemente do tempo de utilização da mesma.</p>

                    <h2>3. Com o meu domínio de site eu já estou protegido?</h2>
                    <p>R: Infelizmente não. Se seu concorrente registrar junto ao INPI – Instituto Nacional da Propriedade Industrial o nome que você utiliza em seu domínio de site ele terá pleno direito constituído em Lei de adquirir o domínio que você havia pago anteriormente.</p>

                    <h2>4. Basta fazer cadastro no INPI – Instituto Nacional da Propriedade Industrial para que minha marca seja registrada?</h2>
                    <p>R: Sua marca estará registrada somente após o deferimento de seu processo de registro, que precisa ser acompanhado detalhadamente durante aproximadamente 24 meses para garantir que sua marca conseguirá concluir todas as etapas processuais do registro.</p>

                    <h2>5. Por que eu preciso contratar uma empresa especializada?</h2>
                    <p>R: Você precisa focar-se nas atividades prestadas por sua empresa e preocupar-se com o sucesso de seu trabalho, por esse motivo  não pode dispor de tempo para acompanhar um processo ao qual você não se especializou.</p>

                    <h2>6. E se eu tentar realizar meu registro de marca sozinho?</h2>
                    <p>R: Você correrá o sério risco de ter sua “idéia” copiada por um concorrente que tenha acessado o site do INPI – Instituto Nacional da Propriedade Industrial e tenha identificado que por falta de acompanhamento especializado o seu processo de registro foi arquivado. Você pode não querer contratar uma empresa perita na área de Marcas e Patentes para cuidar de sua marca, mas seu concorrente pode querer e pode fazer!</p>

                    <h2>7. Sou obrigado a registrar minha marca?</h2>
                    <p>R: De forma alguma! Porém, ao não proteger legalmente sua marca, você estará indiretamente afirmando tanto aos  seus concorrentes e quantos aos seus clientes que o nome de sua empresa não possui valor agregado, o que pode gerar insegurança e a impressão de que o seu trabalho não merece ser devidamente respeitado.</p>

                    <h2>8. Quais os benefícios do registro?</h2>
                    <p>R: Somente uma marca registrada possui proteção do governo federal contra plágios e concorrência desleal. Ao adquirir um produto ou serviço de uma empresa com marca registrada, o cliente certifica-se de que aquela empresa realmente valoriza seu próprio negócio e preocupa-se com a impressão que poderá causar em um mercado tão competitivo quanto o atual.</p>

                    <h2>9. O que acontece quando registro minha marca?</h2>
                    <p>R: Ela obrigatoriamente passa a ser respeita em todo território brasileiro e somente você poderá lucrar com os produtos ou serviços oferecidos através daquele nome comercial. Registrando sua marca você adquire o direito de utilizá-la com exclusividade em sua área de atuação.</p>
